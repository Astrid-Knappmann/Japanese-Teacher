    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        if (areFieldsNull()) {
            JOptionPane.showMessageDialog(this, "All fields must be filled.", "Något har gått fel", JOptionPane.ERROR_MESSAGE);
        } else {
            fCHandler.create(textFieldEnglish.getText(), textFieldRomaji.getText(), textFieldHiragana.getText(), textFieldKanji.getText());
            reset();
        }
    }//GEN-LAST:event_buttonAddActionPerformed

