    public boolean areFieldsNull() {
        if (textFieldEnglish.getText().equalsIgnoreCase("") || textFieldRomaji.getText().equalsIgnoreCase("") || textFieldHiragana.getText().equalsIgnoreCase("") || textFieldKanji.getText().equalsIgnoreCase("")) {
            return true;
        }
        return false;
    }
    
    public void reset(){
        textFieldEnglish.setText("");
        textFieldRomaji.setText("");
        textFieldHiragana.setText("");
        textFieldKanji.setText("");
    }
    public void start(){
        setLocationRelativeTo(null);
        reset();
        setVisible(true);
    }

