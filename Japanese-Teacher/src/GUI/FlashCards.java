    public void nextCard() {
        f = fCHandler.nextCardEWORD();
        if (f != null) {
            eWordLabel.setText(f.geteWord());
            romajiLabel.setText("????");
            hiraganaLabel.setText("????");
            kanjiLabel.setText("?");
            hideCWbuttons();
        } else {
            //To do when everything is answered
            done();

        }
    }

    public void done() {
        buttonWrong.setText("Exit");
        buttonCorrect.setText("Return");
        done = true;
        showCWbuttons();
    }

    public void start(String user) {
        this.user = user;
        fCHandler.loadFlashCards(user);
        reset();
        nextCard();
        setVisible(true);
    }

    public void reset() {
        buttonCorrect.setText("Correct");
        buttonWrong.setText("Wrong");
        done = false;
        labelCorrectScore.setText("0");
        labelWrongScore.setText("0");
        scoreCorrect = 0;
        scoreWrong = 0;
    }

    public void showAnswer() {
        romajiLabel.setText(f.getRomaji());
        hiraganaLabel.setText(f.getHiragana());
        kanjiLabel.setText(f.getKanji());
        showCWbuttons();
    }

    public void hideCWbuttons() {
        buttonCorrect.setVisible(false);
        buttonWrong.setVisible(false);
        buttonAnswered.setVisible(true);

    }

    public void showCWbuttons() {
        buttonCorrect.setVisible(true);
        buttonWrong.setVisible(true);
        buttonAnswered.setVisible(false);
    }

    public void ScoreCorrect() {
        scoreCorrect++;
        labelCorrectScore.setText(Integer.toString(scoreCorrect));
    }

    public void ScoreWrong() {
        scoreWrong++;
        labelWrongScore.setText(Integer.toString(scoreWrong));
    }

