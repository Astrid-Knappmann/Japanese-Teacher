    private void buttonWrongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonWrongActionPerformed
        if (done) {
            System.exit(0);
        } else {
            fCHandler.wrong(f, 0, user);
            ScoreWrong();
            nextCard();
        }
    }//GEN-LAST:event_buttonWrongActionPerformed

    private void buttonCorrectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCorrectActionPerformed
        if (done) {
            this.dispose();
        } else {
            fCHandler.correct(f, 0, user);
            ScoreCorrect();
            nextCard();
        }
    }//GEN-LAST:event_buttonCorrectActionPerformed

